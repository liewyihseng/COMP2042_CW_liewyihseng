package p4_group_8_repo.actor.obstacle;

/**
 * An interface containing the constants of classes
 * that implement Truck Vehicles.
 * @author Liew Yih Seng
 *
 */
public interface TruckVehicle {
	/**
	 * Represents the constant of horizontal width of this {@link TruckVehicle}.
	 */
	final int WIDTH = 120;
	
	/**
	 * Represents the constant of vertical height of this {@link TruckVehicle}.
	 */
	final int HEIGHT = 120;
}